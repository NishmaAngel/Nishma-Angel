    "# Remove any rows with missing values in critical columns\n",
    "initial_rows = len(df)\n",
    "df = df.dropna()\n",
    "print(f\"\\nRows removed due to missing values: {initial_rows - len(df)}\")\n",
    "\n",
    "# Create fiscal year column (assuming fiscal year starts April)\n",
    "df['Fiscal_Year'] = df['Date'].apply(lambda x: x.year if x.month >= 4 else x.year - 1)\n",
    "\n",
    "# Extract temporal features\n",
    "df['Year'] = df['Date'].dt.year\n",
    "df['Month'] = df['Date'].dt.month\n",
    "df['Quarter'] = df['Date'].dt.quarter\n",
    "df['Week'] = df['Date'].dt.isocalendar().week\n",
    "df['Day_of_Week'] = df['Date'].dt.day_name()\n",
    "\n",
    "# Identify outliers using IQR method\n",
    "def identify_outliers(data, column, threshold=1.5):\n",
    "    Q1 = data[column].quantile(0.25)\n",
    "    Q3 = data[column].quantile(0.75)\n",
    "    IQR = Q3 - Q1\n",
    "    lower_bound = Q1 - threshold * IQR\n",
    "    upper_bound = Q3 + threshold * IQR\n",
    "    return data[(data[column] < lower_bound) | (data[column] > upper_bound)]\n",
    "\n",
    "print(\"\\nOutliers Detection (using IQR method):\")\n",
    "for col in ['Gross_Sales', 'Net_Sales', 'Profit_Loss']:\n",
    "    outliers = identify_outliers(df, col)\n",
    "    print(f\"{col}: {len(outliers)} outliers detected ({len(outliers)/len(df)*100:.1f}%)\")\n",
    "\n",
    "print(f\"\\nCleaned Dataset Shape: {df.shape}\")\n",
